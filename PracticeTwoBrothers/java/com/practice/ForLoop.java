package com.practice;

public class ForLoop {
	

		
		public static void main(String[] args) {
			
			for(int i=1; i<=30; i++) {
				
				
				if (i%2 != 0) {
					System.out.println(i + " This is odd number");
				}
				
				else if(i%5 == 0) {
					System.out.println("=============================================");
				}
				else {
					System.out.println(i + " This is even number");
				}
	
			}

			
			
		
		}
		
		
		}

